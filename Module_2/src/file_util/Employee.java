package file_util;

public class Employee {
    private String name;
    private String age;
    private String school;
    private String number;
    private String country;

    public Employee(String name, String age, String school, String number, String country) {
        this.name = name;
        this.age = age;
        this.school = school;
        this.number = number;
        this.country = country;
    }

}
